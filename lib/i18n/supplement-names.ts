/**
 * Translate supplement name from English to Spanish or vice versa
 * @param name - Supplement name in either language
 * @param targetLang - Target language ('en' | 'es')
 * @returns Translated name, or original if no translation found
 */
export function translateSupplementName(
  name: string,
  targetLang: 'en' | 'es'
): string {
  // Check direct match
  const direct = SUPPLEMENT_TRANSLATIONS[name];
  if (direct) {
    return direct[targetLang];
  }

  // Check reverse match (case-insensitive)
  const normalizedName = name.toLowerCase().trim();
  for (const [key, value] of Object.entries(SUPPLEMENT_TRANSLATIONS)) {
    if (
      value.en.toLowerCase() === normalizedName ||
      value.es.toLowerCase() === normalizedName
    ) {
      return value[targetLang];
    }
  }

  // No translation found, return original
  return name;
}

/**
 * Get supplement name in the correct language based on user's language preference
 * @param canonicalName - Canonical name (usually in English)
 * @param userLanguage - User's language preference
 * @returns Localized supplement name
 */
export function getLocalizedSupplementName(
  canonicalName: string,
  userLanguage: 'en' | 'es'
): string {
  return translateSupplementName(canonicalName, userLanguage);
}
