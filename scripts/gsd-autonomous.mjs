#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const ROADMAP_PATH = 'ROADMAP.md';
const VALID_STATUSES = new Set(['HECHO', 'ESPERA_GATE', 'ABIERTA_REAL']);

function parseArgs(argv) {
  const options = {
    recon: false,
    nextOpen: false,
    only: null,
    max: 3,
    dryRun: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    const next = argv[index + 1];

    if (arg === '--recon') {
      options.recon = true;
    } else if (arg === '--next-open') {
      options.nextOpen = true;
    } else if (arg === '--only' && next) {
      options.only = Number(next);
      index += 1;
    } else if ((arg === '--max' || arg === '--max-phases') && next) {
      options.max = Number(next);
      index += 1;
    } else if (arg === '--dry-run') {
      options.dryRun = true;
    }
  }

  return options;
}

function readRoadmap() {
  if (!fs.existsSync(ROADMAP_PATH)) {
    throw new Error(`missing ${ROADMAP_PATH}`);
  }

  return fs.readFileSync(ROADMAP_PATH, 'utf8');
}

function sectionValue(body, heading) {
  const lines = body.split('\n');
  const headingPattern = /^[A-Z][A-Za-z ]+:\s*/;
  const start = lines.findIndex((line) => line.startsWith(`${heading}:`));

  if (start < 0) {
    return '';
  }

  const values = [lines[start].slice(`${heading}:`.length).trim()].filter(Boolean);

  for (let index = start + 1; index < lines.length; index += 1) {
    const line = lines[index];

    if (headingPattern.test(line) || line.startsWith('##')) {
      break;
    }

    values.push(line);
  }

  return values.join('\n').trim();
}

function parsePhases(markdown) {
  const phaseHeading = /^### Phase (\d+) [—-] (.+?) [—-] (HECHO|ESPERA_GATE|ABIERTA_REAL)\s*$/gm;
  const matches = [...markdown.matchAll(phaseHeading)];

  return matches.map((match, index) => {
    const start = match.index + match[0].length;
    const end = index + 1 < matches.length ? matches[index + 1].index : markdown.length;
    const body = markdown.slice(start, end).trim();
    const number = Number(match[1]);
    const slug = match[2].trim();
    const status = match[3].trim();

    if (!VALID_STATUSES.has(status)) {
      throw new Error(`invalid status for phase ${number}: ${status}`);
    }

    return {
      number,
      slug,
      status,
      objective: sectionValue(`\n${body}`, 'Objective'),
      whyOpen: sectionValue(`\n${body}`, 'Why open'),
      whyGated: sectionValue(`\n${body}`, 'Why gated'),
      evidence: sectionValue(`\n${body}`, 'Evidence'),
      closureGate: sectionValue(`\n${body}`, 'Closure gate'),
      nextAction: sectionValue(`\n${body}`, 'Next action'),
    };
  });
}

function summarize(phases) {
  const counts = phases.reduce(
    (acc, phase) => {
      acc[phase.status] = (acc[phase.status] || 0) + 1;
      return acc;
    },
    { HECHO: 0, ESPERA_GATE: 0, ABIERTA_REAL: 0 },
  );

  return {
    roadmap: ROADMAP_PATH,
    phaseCount: phases.length,
    counts,
    nextOpenPhase: phases.find((phase) => phase.status === 'ABIERTA_REAL') || null,
    phases,
  };
}

function specForPhase(phase) {
  return `# TASK_SPEC — ${phase.slug}

## Work Order

Execute ROADMAP Phase ${phase.number}: ${phase.slug}.

## Objective

${phase.objective || 'See ROADMAP.md for phase objective.'}

## Mode

Operate under AGENTS.md and the GSD closure gates. Do not cross human-owned gates.

## In Scope

Derive exact files from ROADMAP Phase ${phase.number} before editing. If exact files cannot be determined from code, mark the phase blocked with an ask.

## Out Of Scope

Merge, deploy, production actions, cloud writes, provider mutations, feature flags, and any unrelated refactor.

## Substitution Test

If this phase is not executed, the ROADMAP remains open at Phase ${phase.number}.

## Closure Gate

${phase.closureGate || '- `npm run gsd:invariants`\n- applicable offline validation\n- read-only fan-out PASS\n- `npm run gsd:done -- --audit-pass-file .planning/<slug>/AUDIT_FANOUT.md`'}

## Driver Evidence

Generated by:

\`\`\`bash
scripts/gsd-autonomous --only ${phase.number}
\`\`\`
`;
}

function ensurePhaseSpec(phase) {
  const planningDir = path.join('.planning', phase.slug);
  const specPath = path.join(planningDir, 'TASK_SPEC.md');
  fs.mkdirSync(planningDir, { recursive: true });

  if (!fs.existsSync(specPath)) {
    fs.writeFileSync(specPath, specForPhase(phase));
  }

  return specPath;
}

function printJson(value) {
  process.stdout.write(`${JSON.stringify(value, null, 2)}\n`);
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  const phases = parsePhases(readRoadmap());

  if (phases.length === 0) {
    throw new Error('no roadmap phases found');
  }

  if (options.nextOpen) {
    const open = phases
      .filter((phase) => phase.status === 'ABIERTA_REAL')
      .slice(0, options.max)
      .map((phase) => phase.number);
    process.stdout.write(`${open.join('\n')}${open.length ? '\n' : ''}`);
    return;
  }

  if (options.recon || options.only === null) {
    printJson(summarize(phases));
    return;
  }

  const phase = phases.find((candidate) => candidate.number === options.only);
  if (!phase) {
    throw new Error(`phase not found: ${options.only}`);
  }

  if (phase.status === 'HECHO') {
    printJson({ phase, terminal: 'done', action: 'do_not_reimplement' });
    return;
  }

  if (phase.status === 'ESPERA_GATE') {
    printJson({ phase, terminal: 'waiting_human_go', action: phase.nextAction || 'wait_for_human_gate' });
    return;
  }

  if (options.dryRun) {
    printJson({ phase, terminal: 'open_real', action: 'would_create_task_spec' });
    return;
  }

  const specPath = ensurePhaseSpec(phase);
  printJson({
    phase,
    terminal: 'task_spec_ready',
    specPath,
    action: 'codex_writer_must_execute_scope_and_closure_gate',
  });
}

try {
  main();
} catch (error) {
  console.error(`GSD_AUTONOMOUS: FAIL ${error instanceof Error ? error.message : String(error)}`);
  process.exit(1);
}
